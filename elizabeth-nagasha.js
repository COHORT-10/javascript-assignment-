let x = 5;
let y = 3;

//addition
console.log('x + y = ', x + y);  //  8

//subtraction
console.log('x - y = ', x - y);  // 2

//multiplication
console.log('x * y = ', x * y);  // 15

//division
console.log('x / y = ', x / y);  // 1.6
//modulus
console.log('x % y = ', x % y);   //2

// increment
console.log('++x = ', ++x); // 6
console.log('x++ = ', x++); // 6
console.log('x = ', x);     // 7

//descrement
console.log('--x = ', --x); // 6
console.log('x-- = ', x--); // 6
console.log('x = ', x);     // 5

//exponential operator 
console.log('x ** y =', x ** y);

//equal to
console.log(2 == 2); // true
console.log(2 == '2'); //true

//not equal to
console.log(3 != 2); // true
console.log('hello' != 'Hello'); //true

// strictlly equal to
console.log(2 === 2); // true
console.log(2 === '2'); // false

//strict not equal to 
console.log(2 !== '2'); // true
console.log(2 !== 2); // false 