//Comments Assignment
let x = 5;//creates a variable x of value 5
let y = 3;//creates a variable y of value 3

// printing the value of the addition of the variables.
console.log('x + y = ', x + y);  //adding(x+y=5+3=8)

//printing the value of the subtraction of the variables.
console.log('x - y = ', x - y);  //(x-y =5-3=2)
//printing the value of the multiplication of the variables.
console.log('x * y = ', x * y);  //(x*y=5*3=15)

//printing the value of the division of the variable x by y.
console.log('x / y = ', x / y);  //(x/y=5/3=1.666...)
//printing the value of the modulus/ remainder of the variables.
console.log('x % y = ', x % y);   // 5%3=5/3=1 remainder 2 so 5%3 =2

// increment
console.log('++x = ', ++x); //increment the values of x by 1 (x+1=5+1=6)
console.log('x++ = ', x++); //increment the values of x by 1 (1+5=1=5=6)
console.log('x = ', x);     //displays the new value of x after incrementing it by (x++ and ++x)=(1+5+1=7)

//decrement operations
console.log('--x = ', --x); // decreases the obtained value of x by1(x-1=7-1=6)
console.log('x-- = ', x--); // decreases the obtained value of x by1(x-1=7-1=6)
console.log('x = ', x);     // displays the value of x that has been obtained after decreasing it by both subtraction operations(7-1-1=5)

// Exponential operators
console.log('x ** y =', x ** y);// displays the value of (x to the power y =5^3=125)

// equal operator
console.log(2 == 2); // displays true because 2 is equal to 2
console.log(2 == '2'); // displays true because 2 is equal to "2"

//not equal to operators
console.log(3 != 2); // displays true because 3 is not equal to 2
console.log('hello' != 'Hello'); //(true because "hello" is not equal to"Hello")

//strictly equal operator
console.log(2 === 2); // true
console.log(2 === '2'); // false

// strictly not equal operator
console.log(2 !== '2'); // true
console.log(2 !== 2); // false 
