let x = 5;
let y = 3;

// this is an addition operator that instructs the computer to add x and y 
console.log('x + y = ', x + y);  // x+y =8

// this is an subtraction operator that instructs the computer to subtract x and y 
console.log('x - y = ', x - y);  // x-y =2

// this is an multiplication operator that instructs the computer to multiple x and y 
console.log('x * y = ', x * y);  // x * y =15

// this is an divison operator that instructs the computer to divide x and y
console.log('x / y = ', x / y);  // x /y = 1.666666667

// this is a remainder operator
console.log('x % y = ', x % y);   //x % y = 2

// increment- operators
//increment operator adds plus 1 to the value of x before runing 
console.log('++x = ', ++x); // ++x =6

// increment operator adds plus 1 to the value of x after runing 
console.log('x++ = ', x++); // x++ =6

// this is the new value of x 
console.log('x = ', x);     // x = 7

// decriment operators 
/// decriment operators subtracts 1 to the new value of x after running
console.log('--x = ', --x); // --x =6

// decriment operators subtracts 1 to the new value of x variable after running
console.log('x-- = ', x--); // x-- = 6

// this is the new value of x variable 
console.log('x = ', x);     //  x =  5

// the 
console.log('x ** y =', x ** y); // x ** y = 125

// These are comparison operators 

// the (==) is an 'equal to' operator
console.log(2 == 2); // true 
console.log(2 == '2'); // true 

// (!=) is a 'not equal to' operator
// this means the value 3 is not equal to 2 
console.log(3 != 2); // true

// this means the 'hello' is not equal to 'Hello'
console.log('hello' != 'Hello'); // true

// (===) is a strict operator that shows that both values must be equal 

console.log(2 === 2); // true

// This is false because the variables are not equal because they are not of the same value type. 
console.log(2 === '2'); // false

// (!==) means not equal 

// this is true because the values are of different value type
console.log(2 !== '2'); // true

// this is false because the values are of the same data type and are equal. 
console.log(2 !== 2); // false