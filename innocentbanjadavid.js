//declaring variables
let x = 5;
let y = 3;

//printing the sum of x and y 
console.log("x + y = ", x + y);     // x + y = 8    

//printing the difference of x and y
console.log("x + y = ", x - y);     // x - y = 2

//printing the product of x to y
console.log("x * y = ", x * y);     // x * y = 15

//printing the division of x by y
console.log("x / y = ", x / y);     // x / y = 1.666666

//printing the modulars of x to y
console.log("x % y = ", x % y);     // x % y = 2

//incrementing
console.log('++x = ', ++x); //6
console.log('x++ = ', x++); //6
console.log('x = ', x);     //7

//decrementing
console.log('--x = ', --x); //6
console.log('x-- = ', x--); //6
console.log('x = ', x);     //5

//printing the x to power y
console.log('x ** y =', x ** y);    //125

//comparing equality
console.log(2 == 2); //true
console.log(2 == '2'); //true

//comparing inequality
console.log(3 != 2); //true
console.log('hello' != 'Hello'); //true

//comparing strict equality
console.log(2 === 2); //true
console.log(2 === '2'); // false

//comparing strict inequality
console.log(2 !== '2'); // true
console.log(2 !== 2); // false
