let x = 5;
let y = 3;

// addition
console.log("x + y = ", x + y); // x + y =  8

// difference
console.log("x - y = ", x - y); // x - y =  2

// multiplication
console.log("x * y = ", x * y); // x * y =  15

// division
console.log("x / y = ", x / y); // x / y =  1.6666666666666667
// modulus of or remainder
console.log("x % y = ", x % y); // x % y =  2

// increment
console.log("++x = ", ++x); // ++x =  6
console.log("x++ = ", x++); // x++ =  6
console.log("x = ", x); // x =  7

// decrement
console.log("--x = ", --x); // --x =  6
console.log("x-- = ", x--); // x-- =  6
console.log("x = ", x); // x =  5

// exponential
console.log("x ** y =", x ** y); // x ** y = 125

// equal to
console.log(2 == 2); // true
console.log(2 == "2"); // true

// not equal to
console.log(3 != 2); // true
console.log("hello" != "Hello"); // true

// strictly equal to
console.log(2 === 2); // true
console.log(2 === "2"); // false

// not strictly equal to
console.log(2 !== "2"); // true
console.log(2 !== 2); // false
