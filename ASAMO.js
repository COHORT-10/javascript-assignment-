let x = 5;
let y = 3;

//addition
console.log('x + y = ', x + y);  // the result is 8

//subtraction
console.log('x - y = ', x - y);  //the result is 2

//multiplication
console.log('x * y = ', x * y);  //the result is 15

//division
console.log('x / y = ', x / y);  //the result is 1.6666667
//modulus
console.log('x % y = ', x % y);   //the result is 2

// increment
console.log('++x = ', ++x); //the result is 6
console.log('x++ = ', x++); //the result is 6
console.log('x = ', x);     //the result is 7

//descrement
console.log('--x = ', --x); //the result is 6
console.log('x-- = ', x--); //the result is 6
console.log('x = ', x);     //the result is 5

//exponentiation operator 
console.log('x ** y =', x ** y);

//equal to
console.log(2 == 2); //is true
console.log(2 == '2'); //is true

//not equal to
console.log(3 != 2); //result is true because the values are different.
console.log('hello' != 'Hello'); //true because the value is different but datatype is the same.

// strict equal operator
console.log(2 === 2); //true because both the value and datatype are exactly the same.
console.log(2 === '2'); // false because the value is the same but datatype is different.

//strict inequality operator
console.log(2 !== '2'); // true because the value is the same but the data type is different.
console.log(2 !== 2); // false because both the value and data type are the same.